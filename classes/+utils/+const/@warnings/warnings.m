% WARNINGS class that defines different warning labels.
% 
% Supported warnings
% 
%  GENERAL - 'LTPDA:General'
%  METHOD_NOT_FOUND  - 'LTPDA:METHOD_NOT_FOUND'
% 
% 

classdef warnings
  properties (Constant = true)
    GENERAL = 'LTPDA:General';
    METHOD_NOT_FOUND = 'LTPDA:Method_Not_Found';
  end
end