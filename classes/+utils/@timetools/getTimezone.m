               'GMT-04' ; ...
               'GMT-03' ; ...
               'GMT-02' ; ...
               'GMT-01' ; ...
               'GMT'    ; ...
               'GMT+01' ; ...
               'GMT+02' ; ...
               'GMT+03' ; ...
               'GMT+04' ; ...
               'GMT+05' ; ...
