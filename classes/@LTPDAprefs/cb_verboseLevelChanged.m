% cb_verboseLevelChanged callback if the user change the verbose level
%
% Parameters:
%       first  - LTPDAprefs object
%       second - Source object (here: java PlotPrefGroup)
%       third  - Event Object (here: PropertyChangeEvent)
%

function cb_verboseLevelChanged(varargin)
  
  clear +utils/@helper/msg_nnl
  clear +utils/@helper/warn
  
end
